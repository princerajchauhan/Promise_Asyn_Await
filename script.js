

// ******************** Question 1 ***********************

let isCode1 = true
function seeHide1(element) {
    if (isCode1) {
        document.getElementById('code1').style.display = "block"
        element.innerText = "Hide Code"
        isCode1 = false
    }
    else{
        document.getElementById('code1').style.display = "none"
        element.innerText = "See Code"
        isCode1 = true
    }
}

